<h4>Application</h4>
<p>When applied to the FX market, for example, you will find the trading range for the session often takes place between the pivot point and the first support and resistance levels. This is because a high number of traders play this range.</p>
<p>It’s also worth noting, this is one of the systems &amp; methods that can be applied to indexes too. For example, it can help form an effective S&amp;P day trading strategy.</p>
