        <div id="section62" class="t2" ><h2>Limit Your Losses</h2>
<p>This is particularly important if you’re using margin. Requirements for which are usually high for day traders. When you trade on margin you are increasingly vulnerable to sharp price movements. Yes, this means the potential for greater profit, but it also means the possibility of significant losses. Fortunately, you can employ stop-losses.</p>
<p>The stop-loss controls your risk for you. In a short position, you can place a stop-loss above a recent high, for long positions you can place it below a recent low. You can also make it dependant on volatility.</p>
<p>For example, a stock price moves by £0.05 a minute, so you place a stop-loss £0.15 away from your entry order, allowing it to swing (hopefully in the expected direction).</p>
<p>One popular strategy is to set up two stop-losses. Firstly, you place a physical stop-loss order at a specific price level. This will be the most capital you can afford to lose. Secondly, you create a mental stop-loss. Place this at the point your entry criteria are breached. So if the trade makes an unanticipated turn, you’ll make a swift exit.</p>
        </div>
  
        <div id="section63" class="t2" ><h2 >Risk Management</h2>
<h3>Stop-loss</h3>
<p>Strategies that work take risk into account. If you don’t manage risk, you’ll lose more than you can afford and be out of the game before you know it. This is why you should always utilise a stop-loss.</p>
<p>The price may look like it’s moving in the direction you hoped, but it could reverse at any time. A stop-loss will control that risk. You’ll exit the trade and only incur a minimal loss if the asset or security doesn’t come through.</p>
<p>Savvy traders don’t usually risk more than 1% of their account balance on a single trade. So if you have £27,500 in your account, you can risk up to £275 per trade.</p>
        </div>
  
        <div id="section64" class="t2" ><h3 id="position-size">Position size</h3>
<p>It will also enable you to select the perfect position size. Position size is the number of shares taken on a single trade. Take the difference between your entry and stop-loss prices. For example, if your entry point is £12 and your stop-loss is £11.80, then your risk is £0.20 per share.</p>
<p>Now to figure out how many trades you can take on a single trade, divide £275 by £0.20. You can take a position size of up to 1,375 shares. That is the maximum position you could take to stick to your 1% risk limit.</p>
<p>Also, check there is sufficient volume in the stock/asset to absorb the position size you use. In addition, keep in mind that if you take a position size too big for the market, you could encounter slippage on your entry and stop-loss.</p>
