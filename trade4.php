<h3>2. Scalping</h3>
<p>One of the most popular strategies is scalping. It’s particularly popular in the forex market, and it looks to capitalise on minute price changes. The driving force is quantity. You will look to sell as soon as the trade becomes profitable. This is a fast-paced and exciting way to trade, but it can be risky. You need a high trading probability to even out the low risk vs reward ratio.</p>
<p>Be on the lookout for volatile instruments, attractive liquidity and be hot on timing. You can’t wait for the market, you need to close losing trades as soon as possible.<br />
