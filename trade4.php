<h3>1. Breakout</h3>
<p>Breakout strategies centre around when the price clears a specified level on your chart, with increased volume. The breakout trader enters into a long position after the asset or security breaks above resistance. Alternatively, you enter a short position once the stock breaks below support.</p>
<p>After an asset or security trades beyond the specified price barrier, volatility usually increases and prices will often trend in the direction of the breakout.</p>
<p>You need to find the right instrument to trade. When doing this bear in mind the asset’s support and resistance levels. The more frequently the price has hit these points, the more validated and important they become.</p>
<h4>Entry Points</h4>
<p>This part is nice and straightforward. Prices set to close and above resistance levels require a bearish position. Prices set to close and below a support level need a bullish position.</p>
<h4>Plan your exits</h4>
<p>Use the asset’s recent performance to establish a reasonable price target. Using chart patterns will make this process even more accurate. You can calculate the average recent price swings to create a target. If the average price swing has been 3 points over the last several price swings, this would be a sensible target. Once you’ve reached that goal you can exit the trade and enjoy the profit.<br />
