<h3>4. Reversal</h3>
<p>Although hotly debated and potentially dangerous when used by beginners, reverse trading is used all over the world. It’s also known as trend trading, pull back trending and a mean reversion strategy.</p>
<p>This strategy defies basic logic as you aim to trade against the trend. You need to be able to accurately identify possible pullbacks, plus predict their strength. To do this effectively you need in-depth market knowledge and experience.</p>
<p>The ‘daily pivot’ strategy is considered a unique case of reverse trading, as it centres on buying and selling the daily low and high pullbacks/reverse.<br />
