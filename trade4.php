<h3>5. Using Pivot Points</h3>
<p>A day trading pivot point strategy can be fantastic for identifying and acting on critical support and/or resistance levels. It is particularly useful in the forex market. In addition, it can be used by range-bound traders to identify points of entry, while trend and breakout traders can use pivot points to locate key levels that need to break for a move to count as a breakout.</p>
<h4>Calculating Pivot Points</h4>
<p>A pivot point is defined as a point of rotation. You use the prices of the previous day’s high and low, plus the closing price of a security to calculate the pivot point.</p>
<p>Note that if you calculate a pivot point using price information from a relatively short time frame, accuracy is often reduced.</p>
<p>So, how do you calculate a pivot point?</p>
<ul>
<li>Central Pivot Point (P) = (High + Low + Close) / 3</li>
</ul>
<p>You can then calculate support and resistance levels using the pivot point. To do that you will need to use the following formulas:</p>
<ul>
<li>First Resistance (R1) = (2*P) &#8211; Low</li>
<li>First Support (S1) = (2*P) &#8211; High</li>
</ul>
<p>The second level of support and resistance is then calculated as follows:</p>
<ul>
<li>Second Resistance (R2) = P + (R1-S1)</li>
<li>Second Support (S2) = P &#8211; (R1- S1)</li>
</ul>
