<h3 >3. Momentum</h3>
<p>Popular amongst trading strategies for beginners, this strategy revolves around acting on news sources and identifying substantial trending moves with the support of high volume. There is always at least one stock that moves around 20-30% each day, so there&#8217;s ample opportunity. You simply hold onto your position until you see signs of reversal and then get out.</p>
<p>Alternatively, you can fade the price drop. This way round your price target is as soon as volume starts to diminish.</p>
<p>This strategy is simple and effective if used correctly. However, you must ensure you’re aware of upcoming news and earnings announcements. Just a few seconds on each trade will make all the difference to your end of day profits.<br />
