            <h1>Basic of company</h1>
<p>Incorporated in 2002, Shyam Metalics and Energy Limited (SMEL) is India's leading integrated metal producer company. The company is primarily engaged in the production of long steel products such as iron pellets, sponge iron, steel billets, TMT, structural products, wire rods, and ferro alloys. As of February 2021, it is among India's largest producers of ferro alloys in terms of installed capacity and the fourth-largest player in the sponge iron industry.</p>
<p>The company serves institutional customers and end-users. Jindal Stainless Limited, Rimjhim Ispat Limit are some of its domestic clients whereas Norecom DMCC, Norecom Limited, POSCO International Corporation, World Metals & Alloys, Traxys North America LLC, JM GLobal Resources, Vijayshri Steel Pvt Ltd, etc. are the international clients. The company has a partnership with 42 distributors to offer its products across 13 states and 1 union territory.</p>
<p>Currently, the firm has 3 manufacturing plants located in Sambalpur in Odisha, and Jamuria and Mangalpur in West Bengal. As of Dec 31, 2020, its manufacturing units have an aggregated installed metal capacity of 5.71 million tonnes per annum. The company is looking to expand its capacity from 5.71 MTPA to 11.60 NTPA by 2025. The Sambalpur and Jamuria plants are integrated steel manufacturing plants that produce captive railway sidings, captive power plants, iron pellet, sponge iron, bilet, thermo mechanically treated (TMT), wire rod, and structural mills, and ferro alloys plants. Further, its manufacturing facility at Mangalpur comprises sponge iron and ferro alloy plants, and a captive power plant.</p>
<p><strong>Competitive Strengths</strong></p>
<ul>
<li>Leading integrated metal producer in India.</li>
<li>Strategically located manufacturing plants with a robust infrastructure.</li>
<li>Diversified product portfolio.</li>
<li>Strong financial performance and distribution network.</li>
</ul>
