            <h1>Basic of company</h1>
<p>Macrotech Developers Limited formerly known as Lodha Developers Limited is a Mumbai based company engaged in the business of real estate development. According to Liases Foras. The company was the largest real estate developer in India by residential Sales for the nine months ended December 31, 2017, and financial year 2017. 

</p><p>The company develops real estate across the residential and commercial sectors in the Mumbai Metropolitan Region (the “MMR”), Pune and London.

</p><p>In our residential portfolio, we are present across multiple price points with unit values ranging from ₹ 3.50 million to ₹ 590 million. In our commercial portfolio, we develop office and retail projects as income-generating assets on lease model and sale model, with an increasing focus on the former.
</p>

<ul>
<li>37 ongoing projects.</li>
<li>54.77 million square feet developable area of completed project.</li>
<li>3,700 direct employees in India.</li>
<li>30,300 apartments sold at Palava City since launch.</li>
<li>INR 3.50 million to INR 590 million range of affordable to high-end luxury residences.</li>
</ul>

