            <h1>Basic of company</h1>
            <h1><strong>Barbeque Nation IPO</strong></h1>
<p>Barbeque Nation Hospitality Limited owns and operates <strong>Barbeque Nation Restaurants</strong>, one of India’s leading casual dining restaurant chains. They also own and operate <strong>Toscano Restaurants</strong> and <strong>UBQ by Barbeque Nation.</strong> </p>
<p>The Company launched its first Barbeque Nation Restaurant in 2008 and subsequently acquired 5 Barbeque Nation Restaurants in 2012. They have steadily grown to 138 Barbeque Nation Restaurants in 73 cities in India and 7 International Barbeque Nation Restaurants in 3 countries outside India as of November 30, 2019.</p>
<p>Its recently acquired 61.35%  Subsidiaries, Red Apple, owns and operates 9 restaurants under the brand name, “Toscano”, a casual dining Italian restaurant chain and operates a single restaurant under the brand name “La Terrace”. As of November 30, 2019, they operated 10 Italian cuisine restaurants, 9 of which are under the brand name “Toscano”, in 2 cities in India. In November 2018, they launched UBQ by Barbeque Nation to provide a la carte Indian cuisine in the value segment. At present, UBQ by Barbeque Nation predominantly caters to the delivery segment.<br />
</p>


