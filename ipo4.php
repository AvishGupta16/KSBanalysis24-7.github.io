<p>Powergrid InvIT's sponsor is Power Grid Corporation of India Limited (PGCIL). PGCIL is the largest power transmission company in India. PGCIL is engaged in project planning, designing, financing, constructing, operating, and maintaining power transmission projects across India.</p>
<p>Powergrid InvIT's Investment Manager, POWERGRID Unchahar Transmission Limited, a wholly-owned subsidiary of PGCIL. They are responsible for managing the InvIT, the Initial Portfolio Assets as well as undertaking investment decisions relating to its assets.</p>
<p>Powergrid InvIT's Trustee, IDBI Trusteeship Services Limited is SEBI registered debenture trustee. They are responsible for ensuring that its business activities and investment policies comply with the provisions of the InvIT Regulations, and monitoring the activities of our Investment Manager and the Project Manager.</p>
